public class Person {
    private String Phone;
    private String name;

    public void setPhone(String phone) {
        Phone = phone;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return Phone;
    }

    public String getName() {
        return name;
    }

}
